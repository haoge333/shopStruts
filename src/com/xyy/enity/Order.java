package com.xyy.enity;

public class Order {
	private Integer id;           //订单编号
	private String orderNumber;   //交易流水号
	private Double totalPrice;    //订单总价
	private Integer userId;       //客户编号
	private Integer addressId;    //地址编号
	private String orderDate;     //订单日期
	private String status;       //订单的状态
	
	
	public Order() {
		super();
	}


	public Order(Integer id, String orderNumber, Double totalPrice,
			Integer userId, Integer addressId, String orderDate, String status) {
		super();
		this.id = id;
		this.orderNumber = orderNumber;
		this.totalPrice = totalPrice;
		this.userId = userId;
		this.addressId = addressId;
		this.orderDate = orderDate;
		this.status = status;
	}


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getOrderNumber() {
		return orderNumber;
	}


	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}


	public Double getTotalPrice() {
		return totalPrice;
	}


	public void setTotalPrice(Double totalPrice) {
		this.totalPrice = totalPrice;
	}


	public Integer getUserId() {
		return userId;
	}


	public void setUserId(Integer userId) {
		this.userId = userId;
	}


	public Integer getAddressId() {
		return addressId;
	}


	public void setAddressId(Integer addressId) {
		this.addressId = addressId;
	}


	public String getOrderDate() {
		return orderDate;
	}


	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}


	public String getStatus() {
		return status;
	}


	public void setStatus(String status) {
		this.status = status;
	}


	@Override
	public String toString() {
		return "Order [id=" + id + ", orderNumber=" + orderNumber
				+ ", totalPrice=" + totalPrice + ", userId=" + userId
				+ ", addressId=" + addressId + ", orderDate=" + orderDate
				+ ", status=" + status + "]";
	}


	

}
