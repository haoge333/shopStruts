package com.xyy.enity;

import java.io.File;
import java.io.Serializable;

public class Shopping implements Serializable{

	private static final long serialVersionUID = -3186260035823288330L;
	
	private Integer id;
	private String name;
	private Double price;
	private String brand;
	private String createDate;
	private String description;
	private String categoryName;
	private String imgsFileName;
	private File imgs;


	public Shopping() {
		super();
	}


	public Shopping(Integer id, String name, Double price, String brand,
			String createDate, String description, String categoryName,
			String imgsFileName, File imgs) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
		this.brand = brand;
		this.createDate = createDate;
		this.description = description;
		this.categoryName = categoryName;
		this.imgsFileName = imgsFileName;
		this.imgs = imgs;
	}


	public Shopping(Integer id, String name, Double price, String brand,
			String createDate, String description, String categoryName,
			String imgsFileName) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
		this.brand = brand;
		this.createDate = createDate;
		this.description = description;
		this.categoryName = categoryName;
		this.imgsFileName = imgsFileName;
	}


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public Double getPrice() {
		return price;
	}


	public void setPrice(Double price) {
		this.price = price;
	}


	public String getBrand() {
		return brand;
	}


	public void setBrand(String brand) {
		this.brand = brand;
	}


	public String getCreateDate() {
		return createDate;
	}


	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}


	public String getDescription() {
		return description;
	}


	public void setDescription(String description) {
		this.description = description;
	}


	public String getCategoryName() {
		return categoryName;
	}


	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}


	public String getImgsFileName() {
		return imgsFileName;
	}


	public void setImgsFileName(String imgsFileName) {
		this.imgsFileName = imgsFileName;
	}


	public File getImgs() {
		return imgs;
	}


	public void setImgs(File imgs) {
		this.imgs = imgs;
	}


	@Override
	public String toString() {
		return "Shopping [id=" + id + ", name=" + name + ", price=" + price
				+ ", brand=" + brand + ", createDate=" + createDate
				+ ", description=" + description + ", categoryName="
				+ categoryName + ", imgsFileName=" + imgsFileName + ", imgs="
				+ imgs + "]";
	}


	
	
	
	
	


	
	
}
